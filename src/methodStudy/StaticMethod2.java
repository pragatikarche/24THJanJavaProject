package methodStudy;

public class StaticMethod2 {
    public static void b()
    {
        System.out.println("Static regular method --> b another class");
    }

    public static void c()
    {
        System.out.println("Static regular method -->c another class=");
    }

    public  void a()
    {
        System.out.println("Static regular method -->a from another class ");
    }

    public  void d() {
        System.out.println("Static regular method --> d ");
    }
}
