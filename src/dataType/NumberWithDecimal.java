package dataType;

public class NumberWithDecimal {

    public static void main(String[] args)
    {
        //declare variable with int datatype
        int totalMark;
        //assign variable with int datatype
        totalMark = 120;
        System.out.println(totalMark);
        System.out.println("Total Marks is=");
        System.out.println("Total Marks is="+totalMark);
        //declare variable with float Datatype
        float temp;
        //assign variable with float datatype
        temp = 18.8f;
        System.out.println(temp);
        System.out.println("Temperature value is=");
        System.out.println("Temperature value is="+temp);
        // declare variable with double datatype
        double salary;
        //assign verable with double datatype
        salary = 68890.74d;
        System.out.println(salary);
        System.out.println("My salary is=");
        System.out.println("My salary is= "+salary);

    }
}
