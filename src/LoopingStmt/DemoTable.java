package LoopingStmt;

public class DemoTable
{
    public static void main(String[] args)
    {
        System.out.println("*****Table for 3*****");
        for (int i = 3; i<=30; i +=3)
        {
            System.out.println(i);
        }
        System.out.println("*****Odd Reverse Number*****");

        for (int a=10;a>=1;a--)
        {
            System.out.println(a);
        }
    }
}
